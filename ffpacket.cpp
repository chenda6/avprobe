#include <iostream>
#include <string>
#include "ffpacket.h"

using namespace avprobe;


//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
FFPacket::FFPacket(const AVPacket &packet)
: mPacket(packet)
{
}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
FFPacket::~FFPacket()
{
    av_packet_unref(&mPacket);
}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
int64_t FFPacket::dts() const
{
    return mPacket.dts;
}

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
int64_t FFPacket::pts() const
{
    return mPacket.pts;
}